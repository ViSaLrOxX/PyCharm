import numpy as np
#Asking user to enter the number “x”
num_x = int(input("Enter number x: "))
#Asking the user to enter the number “y”
num_y = int(input("Enter number y: "))
#Printing out number “x”, raised to the power “y”.
print("x**y= ", num_x ** num_y)
#Printing out the log (base 2) of “x”.
print("log(x) =", numpy.log2(num_x))